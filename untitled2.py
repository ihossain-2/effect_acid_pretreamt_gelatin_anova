def format_p(p):
    """Format p-value for display."""
    if pd.isna(p):
        return "n/a"
    if p < 0.001:
        return "< 0.001"
    elif p < 0.01:
        return "< 0.01"
    elif p < 0.05:
        return "< 0.05"
    else:
        return f"{p:.3f}"

def sig_code(p):
    """Return significance code."""
    if pd.isna(p):
        return "n/a"
    if p < 0.001:
        return "***"
    elif p < 0.01:
        return "**"
    elif p < 0.05:
        return "*"
    else:
        return "ns"

def run_oneway_anova(df, group_cols, response):
    """
    Group data by group_cols, then run one-way ANOVA across those groups.
    This approximates factorial ANOVA for each effect.
    """
    groups = [grp[response].values
              for _, grp in df.groupby(group_cols)
              if len(grp[response]) > 0]
    if len(groups) < 2:
        return np.nan, np.nan
    try:
        F, p = stats.f_oneway(*groups)
        return F, p
    except Exception:
        return np.nan, np.nan


