            ss_val  = f"{row['sum_sq']:.3f}" if not pd.isna(row['sum_sq'])   else "—"
            ms_val  = f"{row['mean_sq']:.3f}"if not pd.isna(row['mean_sq'])  else "—"
            f_val   = f"{row['F']:.3f}"      if not pd.isna(row['F'])        else "—"
            p_val   = format_p(row['PR(>F)'])if not pd.isna(row['PR(>F)'])  else "—"
